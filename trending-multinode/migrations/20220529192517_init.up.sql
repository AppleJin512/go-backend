CREATE TABLE collections
(
    id              BIGSERIAL,
    symbol          VARCHAR(500),
    name            VARCHAR(500),
    image           VARCHAR(500),
    watchlist_count INT DEFAULT 0,
    PRIMARY KEY (id)
);

CREATE TABLE activities
(
    id         BIGSERIAL,
    signature  VARCHAR(500),
    symbol     VARCHAR(500),
    block_time TIMESTAMP,
    price      DECIMAL DEFAULT 0,
    PRIMARY KEY (id)
);

-- ---------------------------------------------------------------------------------------------------------------------
-- Indexes
-- ---------------------------------------------------------------------------------------------------------------------

-- ---------------------------------------------------------------------------------------------------------------------
-- Unique indexes
-- ---------------------------------------------------------------------------------------------------------------------
CREATE UNIQUE INDEX iux_collections_symbol ON collections (symbol);
CREATE UNIQUE INDEX iux_activities_signature ON activities(signature);

